// Get month with most birthdays
export function getMostPopularMonth() {
  const counts = {};
  for (let month = 1; month <= 12; month++) {
    counts[month] = getBirthdaysByMonth(month).length;
  }
  const maxMonth = Object.entries(counts).reduce((a, b) => a[1] > b[1] ? a : b)[0];
  return MONTH_NAMES[parseInt(maxMonth) - 1];
}

// Get birthday stats
export function getBirthdayStats() {
  const living = drummerBirthdays.filter(d => d.isLiving).length;
  const total = drummerBirthdays.length;
  const avgAge = Math.round(
    drummerBirthdays
      .filter(d => d.isLiving)
      .reduce((sum, d) => sum + calculateAge(d.birthYear, d.birthMonth, d.birthDay), 0) / living
  );
  
  return {
    total,
    living,
    deceased: total - living,
    avgAge,
    oldestDrummer: drummerBirthdays.filter(d => d.isLiving).reduce((oldest, d) => 
      d.birthYear < oldest.birthYear ? d : oldest
    ),
    youngestDrummer: drummerBirthdays.filter(d => d.isLiving).reduce((youngest, d) => 
      d.birthYear > youngest.birthYear ? d : youngest
    ),
    mostPopularMonth: getMostPopularMonth()
  };
}

