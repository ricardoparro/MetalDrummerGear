/**
 * Get events filtered by decade
 */
export function getEventsByDecade(decade) {
  return EVOLUTION_TIMELINE.filter(event => event.decade === decade);
}

/**
 * Get events filtered by subgenre
 */
export function getEventsBySubgenre(subgenre) {
  return EVOLUTION_TIMELINE.filter(event => event.subgenre === subgenre);
}

/**
 * Get events filtered by type
 */
export function getEventsByType(type) {
  return EVOLUTION_TIMELINE.filter(event => event.type === type);
}

/**
 * Get events for a specific year
 */
export function getEventsByYear(year) {
  return EVOLUTION_TIMELINE.filter(event => event.year === year);
}

/**
 * Get event by ID
 */
export function getEventById(id) {
  return EVOLUTION_TIMELINE.find(event => event.id === id);
}

/**
 * Search timeline events
 */
export function searchTimeline(query) {
  const lowerQuery = query.toLowerCase();
  return EVOLUTION_TIMELINE.filter(event => 
    event.title.toLowerCase().includes(lowerQuery) ||
    event.drummerName.toLowerCase().includes(lowerQuery) ||
    event.band.toLowerCase().includes(lowerQuery) ||
    event.description.toLowerCase().includes(lowerQuery) ||
    (event.album && event.album.toLowerCase().includes(lowerQuery))
  );
}

