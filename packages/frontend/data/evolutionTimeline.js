/**
 * Check if we're on the timeline page
 */
export function isTimelinePage() {
  if (typeof window === 'undefined') return false;
  const pathname = window.location.pathname;
  return pathname === '/history' || pathname === '/history/' ||
         pathname === '/timeline' || pathname === '/timeline/';
}

/**
 * Navigate to timeline page
 */
export function navigateToTimeline(decade = null) {
  if (typeof window === 'undefined') return;
  const path = decade ? `/history?decade=${decade}` : '/history';
  window.history.pushState({}, '', path);
  window.dispatchEvent(new PopStateEvent('popstate'));
}

/**
 * Get URL parameters for sharing
 */
export function getTimelineShareUrl(eventId = null, decade = null) {
  let url = 'https://metalforge.io/history';
  if (eventId) {
    url += `?event=${eventId}`;
  } else if (decade) {
    url += `?decade=${decade}`;
  }
  return url;
}
