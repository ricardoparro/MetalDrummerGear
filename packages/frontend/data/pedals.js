/** drummerSlug -> pedal record, derived from PEDALS. */
export const DRUMMER_PEDAL = PEDALS.reduce((map, pedal) => {
  map[pedal.drummerSlug] = pedal;
  return map;
}, {});

export function getPedalForDrummer(drummerSlug) {
  return DRUMMER_PEDAL[drummerSlug] || null;
}

