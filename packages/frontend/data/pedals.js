/**
 * Bass drum pedal data module — foundation for the /pedals content hub.
 * Issue #4387 (phase 1/4): single-source-of-truth data module, deterministically
 * parsed from the roster's `gear.hardware` strings in api/drummers/index.js. Hub
 * pages, drummer setup pages, and brand/best-for-metal pages are separate
 * follow-up issues (phases 2-4).
 *
 * Pedal record shape:
 *   {
 *     drummerSlug: string,       // toSlug(drummer.name), matches snares.js/
 *                                 // cymbalSetups.js convention
 *     summary: string,           // the pedal-specific segment of the roster
 *                                 // gear.hardware string, verbatim (thrones,
 *                                 // racks, hi-hat stands, and electronics that
 *                                 // share the same field are excluded)
 *     brand: string | null,      // manufacturer, when the summary starts with a
 *                                 // recognized brand name; null when the source
 *                                 // text names more than one candidate brand
 *                                 // (e.g. "DW 9000 or Tama Iron Cobra")
 *     model: string | null,      // remaining descriptive text (never invented —
 *                                 // copied verbatim, minus the brand/pedal-count
 *                                 // tokens)
 *     configuration: 'double-pedal' | 'dual-single-pedals' | 'single-pedal' | null,
 *                                // 'double-pedal' for a single chassis driving
 *                                 // both beaters via a slave linkage; 'dual-single-
 *                                 // pedals' when the summary literally lists two
 *                                 // independent single pedals (x2); 'single-pedal'
 *                                 // for one beater; null when the source text
 *                                 // doesn't say (never guessed from reputation)
 *     coDesigned: boolean,       // true only when "Co-designed" appears verbatim
 *                                 // in the summary (signature/artist-collab pedal)
 *     source: string,            // provenance note pointing back to the roster record
 *     retailerUrls: {},          // intentionally empty until affiliate links are added
 *   }
 *
 * Only roster drummers with `gear.verified: true` are parsed here (56 of 67),
 * matching the snares.js (#4308) and cymbalSetups.js (#4303) audit exactly.
 * Unverified drummers (wait for a research pass before their pedals are added;
 * never guessed): ray-luzier, vinnie-paul, charlie-benante, mike-portnoy,
 * danny-carey, brann-dailor, matt-halpern, pete-sandoval, arin-ilejay,
 * nick-augusto, isaac-lamb.
 *
 * Parsing is conservative: `brand` and `configuration` are left null whenever
 * the source text itself is ambiguous (e.g. an "or" between two brand names,
 * or a plural "Pedals" with no double/single qualifier) rather than guessing.
 * No pedal drive mechanism (chain/belt/direct-drive) is recorded here — none
 * of the roster's gear.hardware strings state it, and it is never inferred
 * from brand reputation; that comparison belongs to the /pedals/drive-types
 * reference page (phase 2).
 */

