/**
 * Snare data module — foundation for the /snares content hub.
 * Issue #4308 (phase 1/4): single-source-of-truth data module, deterministically
 * parsed from the roster's `gear.snare` strings in api/drummers/index.js. Hub
 * pages, reference pages, and signature-drummer pages are separate follow-up
 * issues (phases 2-4).
 *
 * Snare record shape:
 *   {
 *     drummerSlug: string,       // toSlug(drummer.name), matches drumsticks.js/
 *                                 // cymbalSetups.js convention
 *     summary: string,           // the original roster gear.snare string, verbatim
 *     brand: string | null,      // manufacturer, when the summary starts with a
 *                                 // recognized brand name
 *     model: string | null,      // remaining descriptive text (never invented —
 *                                 // copied verbatim, minus the brand/size tokens)
 *     sizeIn: number,            // primary diameter, inches
 *     depthIn: number,           // primary depth, inches
 *     shellMaterial: string | null,
 *                                // only set when a shell-material word (Brass,
 *                                 // Steel, Maple, Birch, Bronze, Walnut, Bubinga,
 *                                 // etc.) appears verbatim in the summary —
 *                                 // never guessed from brand/model reputation
 *     isSignature: boolean,      // true only when "Signature" appears verbatim
 *                                 // in the summary
 *     source: string,            // provenance note pointing back to the roster record
 *     retailerUrls: {},          // intentionally empty until affiliate links are added
 *   }
 *
 * Only roster drummers with `gear.verified: true` are parsed here (56 of 67),
 * matching the cymbalSetups.js (#4303) audit exactly.
 * Unverified drummers (wait for a research pass before their snares are added;
 * never guessed): ray-luzier, vinnie-paul, charlie-benante, mike-portnoy,
 * danny-carey, brann-dailor, matt-halpern, pete-sandoval, arin-ilejay,
 * nick-augusto, isaac-lamb.
 *
 * 14 roster drummers have a literal "Signature" snare model, but 4 of them
 * (Vinnie Paul, Charlie Benante, Mike Portnoy, Danny Carey) are unverified and
 * excluded above — so 10 signature snares are flagged (`isSignature: true`)
 * in this module. The flagship /snares/signature/<drummer> pages (phase 3)
 * read from this subset.
 *
 * Parsing is conservative: size is only recorded for the first size mentioned
 * when a summary lists more than one snare (e.g. a rack/piccolo pairing), and
 * shellMaterial is left null whenever the source text itself is ambiguous
 * (e.g. "Metal or Maple") rather than guessing a single material.
 */

